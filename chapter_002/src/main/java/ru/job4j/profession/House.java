public class House {
    public String name;
    public int floors;

    public String getName() {
        return this.name;
    }

    public String setName(String name) {
        return this.name = name;
    }

    public int getFloors() {
        return this.floors;
    }

    public int setFloors(int floors) {
        return  this.floors = floors;
    }
}
